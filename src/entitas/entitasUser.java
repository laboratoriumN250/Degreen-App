public class entitasUser {
    String ID, Username, Password, Nama,Level, MenuTamu, MenuUser, MenuPemesanan, MenuLaporan;

    public String getLevel() {
        return Level;
    }

    public void setLevel(String Level) {
        this.Level = Level;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getUsername() {
        return Username;
    }

    public void setUsername(String Username) {
        this.Username = Username;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String Password) {
        this.Password = Password;
    }

    public String getNama() {
        return Nama;
    }

    public void setNama(String Nama) {
        this.Nama = Nama;
    }

    public String getMenuTamu() {
        return MenuTamu;
    }

    public void setMenuTamu(String MenuTamu) {
        this.MenuTamu = MenuTamu;
    }

    public String getMenuUser() {
        return MenuUser;
    }

    public void setMenuUser(String MenuUser) {
        this.MenuUser = MenuUser;
    }

    public String getMenuPemesanan() {
        return MenuPemesanan;
    }

    public void setMenuPemesanan(String MenuPemesanan) {
        this.MenuPemesanan = MenuPemesanan;
    }

    public String getMenuLaporan() {
        return MenuLaporan;
    }

    public void setMenuLaporan(String MenuLaporan) {
        this.MenuLaporan = MenuLaporan;
    }
}
